# [20] 有效的括号
#

# @lc code=start
class Solution:
    def isValid(self, s: str) -> bool:
        stack = []
        for item in s:
            if item == '(':
                stack.append(')')
            elif item == '{':
                stack.append('}')
            elif item == '[':
                stack.append(']')
            elif stack and stack[-1] == item:
                stack.pop()
            else:
                return False
        if not stack:
            return True
        else:
            return False

